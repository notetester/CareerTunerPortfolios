CRITERIA = [
    "GOAL_CLARITY",
    "EXPERIENCE_SPECIFICITY",
    "ACHIEVEMENT_EVIDENCE",
    "JOB_SKILL_ALIGNMENT",
    "DOCUMENT_CONSISTENCY",
    "IMPROVEMENT_READINESS",
]

SYSTEM_PROMPT = (
    "너는 CareerTuner 프로필 평가 모델이다. 응답은 반드시 summary, extractedSkills, "
    "strengths, gaps, recommendations, criterionScores를 가진 JSON 객체 하나로만 작성한다. "
    "criterionScores에는 GOAL_CLARITY, EXPERIENCE_SPECIFICITY, ACHIEVEMENT_EVIDENCE, "
    "JOB_SKILL_ALIGNMENT, DOCUMENT_CONSISTENCY, IMPROVEMENT_READINESS 6개 기준을 모두 포함한다."
)


def profile(
    desired_job,
    desired_industry,
    education,
    career,
    projects,
    skills,
    resume_text,
    self_intro,
    certificates=None,
    languages=None,
    portfolio_slug=None,
):
    return {
        "desiredJob": desired_job,
        "desiredIndustry": desired_industry,
        "education": education,
        "career": career,
        "projects": projects,
        "skills": skills,
        "certificates": certificates or [],
        "languages": languages or [],
        "portfolioLinks": [f"https://example.com/{portfolio_slug}"] if portfolio_slug else [],
        "resumeText": resume_text,
        "selfIntro": self_intro,
        "preferences": {"region": "수도권", "workType": "정규직"},
    }


def criterion_scores(scores, evidence_base, improvement_base):
    return [
        {
            "criterion": criterion,
            "rawScore": scores[criterion],
            "evidence": evidence_base[criterion],
            "improvement": improvement_base[criterion],
        }
        for criterion in CRITERIA
    ]


def sample(job_family, profile_data, summary, extracted, strengths, gaps, recommendations, scores, evidence, improvement):
    user_payload = {
        "featureType": "PROFILE_COMPLETENESS",
        "jobFamily": job_family,
        "profile": profile_data,
    }
    assistant_payload = {
        "summary": summary,
        "extractedSkills": extracted,
        "strengths": strengths,
        "gaps": gaps,
        "recommendations": recommendations,
        "criterionScores": criterion_scores(scores, evidence, improvement),
    }
    return {
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": json.dumps(user_payload, ensure_ascii=False)},
            {"role": "assistant", "content": json.dumps(assistant_payload, ensure_ascii=False)},
        ]
    }


